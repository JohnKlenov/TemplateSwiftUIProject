    // MARK: - Anonymous ‚Üí Link (SignUp)
    private func googleLinkAnonymous(user: User, credential: AuthCredential) -> AnyPublisher<Void, Error> {
        Future<Void, Error> { [weak self] promise in
            user.link(with: credential) { res, err in
                if let err = err {
                    // –ï—Å–ª–∏ credential —É–∂–µ –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è –¥—Ä—É–≥–∏–º UID ‚Üí —ç—Ç–æ –Ω–µ –ª–∏–Ω–∫–æ–≤–∫–∞; –≤ —Ç–≤–æ–µ–π –ø–æ–ª–∏—Ç–∏–∫–µ –ª–∏–Ω–∫–æ–≤–∫—É –Ω–µ –ø—Ä–æ–¥–æ–ª–∂–∞–µ–º
                    // –ú–æ–∂–Ω–æ —Å–æ–æ–±—â–∏—Ç—å –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é, —á—Ç–æ —ç—Ç–æ—Ç Google-–∞–∫–∫–∞—É–Ω—Ç —É–∂–µ –∑–∞—Ä–µ–≥–∏—Å—Ç—Ä–∏—Ä–æ–≤–∞–Ω ‚Äî –∏—Å–ø–æ–ª—å–∑—É–π—Ç–µ Sign In.
