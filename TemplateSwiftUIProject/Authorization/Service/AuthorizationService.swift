    /// 3. –ü—ã—Ç–∞–µ–º—Å—è –Ω–∞–π—Ç–∏ keyWindow —Å—Ä–µ–¥–∏ –∞–∫—Ç–∏–≤–Ω—ã—Ö —Å—Ü–µ–Ω –∏ –±–µ—Ä—ë–º –µ–≥–æ rootViewController.
    /// 4. –ï—Å–ª–∏ keyWindow –Ω–µ –Ω–∞–π–¥–µ–Ω, –∏—Å–ø–æ–ª—å–∑—É–µ–º –ø–µ—Ä–≤—ã–π –≤–∏–¥–∏–º—ã–π (–Ω–µ —Å–∫—Ä—ã—Ç—ã–π, alpha > 0) window –∏ –µ–≥–æ rootViewController.
    /// 5. –†–µ–∫—É—Ä—Å–∏–≤–Ω–æ –ø–æ–¥–Ω–∏–º–∞–µ–º—Å—è –ø–æ –∏–µ—Ä–∞—Ä—Ö–∏–∏ –∫–æ–Ω—Ç—Ä–æ–ª–ª–µ—Ä–æ–≤:
    ///    - –ï—Å–ª–∏ —ç—Ç–æ UINavigationController ‚Üí –≤–æ–∑–≤—Ä–∞—â–∞–µ–º visibleViewController.
    ///    - –ï—Å–ª–∏ UITabBarController ‚Üí –≤–æ–∑–≤—Ä–∞—â–∞–µ–º selectedViewController.
    ///    - –ï—Å–ª–∏ –µ—Å—Ç—å presentedViewController ‚Üí –≤–æ–∑–≤—Ä–∞—â–∞–µ–º –µ–≥–æ.
    ///    - –ò–Ω–∞—á–µ –≤–æ–∑–≤—Ä–∞—â–∞–µ–º –±–∞–∑–æ–≤—ã–π –∫–æ–Ω—Ç—Ä–æ–ª–ª–µ—Ä.
    ///
    /// –ü—Ä–∏–º–µ—á–∞–Ω–∏—è:
    /// - –ú–µ—Ç–æ–¥ —É—Å—Ç–æ–π—á–∏–≤ –∫ –º—É–ª—å—Ç–∏-—Å—Ü–µ–Ω–∞–º (iPad, multi-window) –∏ –≤—Ä–µ–º–µ–Ω–Ω—ã–º —Å–æ—Å—Ç–æ—è–Ω–∏—è–º UI.
    /// - –í—ã–∑—ã–≤–∞—Ç—å –Ω—É–∂–Ω–æ –Ω–∞ –≥–ª–∞–≤–Ω–æ–º –ø–æ—Ç–æ–∫–µ –ø–µ—Ä–µ–¥ –ø–æ–∫–∞–∑–æ–º UI.
    /// - –õ–æ–≥–∏ –ø–æ–º–æ–≥–∞—é—Ç –æ—Ç—Å–ª–µ–¥–∏—Ç—å –ø—É—Ç—å –≤—ã–±–æ—Ä–∞ –∫–æ–Ω—Ç—Ä–æ–ª–ª–µ—Ä–∞; –≤ –ø—Ä–æ–¥–∞–∫—à–µ–Ω–µ –º–æ–∂–Ω–æ –æ–±–µ—Ä–Ω—É—Ç—å –∏—Ö –≤ `#if DEBUG`.
    static func topViewController(base: UIViewController? = {
        // MARK: —Å–±–æ—Ä —Å—Ü–µ–Ω
        let allScenes = UIApplication.shared.connectedScenes

        let activeScenes = allScenes
            .compactMap { $0 as? UIWindowScene }
            .filter { $0.activationState == .foregroundActive }

        // MARK: –ü–æ–ø—ã—Ç–∫–∞ 1: keyWindow
        if let keyWindow = activeScenes
            .flatMap({ $0.windows })
            .first(where: { $0.isKeyWindow }) {
            let root = keyWindow.rootViewController
            return root
        }

        // MARK: –ü–æ–ø—ã—Ç–∫–∞ 2: –ø–µ—Ä–≤—ã–π –≤–∏–¥–∏–º—ã–π window
        if let visibleWindow = activeScenes
            .flatMap({ $0.windows })
            .first(where: { !$0.isHidden && $0.alpha > 0 }) {
            let root = visibleWindow.rootViewController
            return root
        }

        // MARK: –ü–æ–ø—ã—Ç–∫–∞ 3: fallback –±–µ–∑ —É—Å—Ç–∞—Ä–µ–≤—à–µ–≥–æ API
        return nil
    }()) -> UIViewController? {
        // –†–µ–∫—É—Ä—Å–∏–≤–Ω–æ –æ–ø—Ä–µ–¥–µ–ª—è–µ–º –≤–µ—Ä—Ö–Ω–∏–π VC
        if let nav = base as? UINavigationController {
            return topViewController(base: nav.visibleViewController)
        }
        if let tab = base as? UITabBarController {
            return topViewController(base: tab.selectedViewController)
        }
        if let presented = base?.presentedViewController {
            return topViewController(base: presented)
        }
        return base
    }
}

