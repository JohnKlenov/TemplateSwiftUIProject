==============================================================
🎧 СПОТИФАЙ — РАСШИРЕННОЕ ОБЪЯСНЕНИЕ ПО OAUTH, ЛИЦЕНЗИИ И SDK
==============================================================

Этот документ подробно объясняет три ключевых юридических и технических блока,
которые критически важны для создания приложения, использующего Spotify API:

1. OAuth и безопасная работа с токенами
2. Лицензия Spotify API и связанные ограничения
3. Воспроизведение музыки через официальные Spotify SDK

==============================================================
1. OAUTH — ПОЛНОЕ ОБЪЯСНЕНИЕ
==============================================================

OAuth — это единственный легальный способ получить доступ к данным пользователя
и управлять воспроизведением от его имени. Приложение НЕ имеет права спрашивать
логин/пароль Spotify — только официальный OAuth.

----------------------------
A) Как работает OAuth
----------------------------
• Пользователь нажимает “Войти через Spotify”.
• Его перенаправляет на официальный сайт Spotify.
• Он подтверждает разрешения (scopes).
• Spotify возвращает вашему приложению временный код.
• Код обменивается на access token и refresh token.

----------------------------
B) Почему нужен PKCE
----------------------------
PKCE — это защита OAuth для мобильных приложений.
Он предотвращает перехват кода злоумышленником.
Использовать PKCE — обязательная практика безопасности.

----------------------------
C) Access token
----------------------------
• Живёт недолго (обычно 1 час).
• Используется для запросов к API.
• Хранится в защищённом хранилище (Keychain / Keystore).
• НЕ логируется, НЕ передаётся в открытом виде.

----------------------------
D) Refresh token
----------------------------
• Позволяет получать новые access tokens.
• Должен храниться максимально безопасно.
• Идеально — хранить на backend‑сервере.
• При каждом обновлении можно использовать rotation (старый токен аннулируется).

----------------------------
E) Ошибки и безопасность
----------------------------
• Если refresh token просрочен — пользователь должен пройти OAuth заново.
• Если токены украдены — злоумышленник может управлять Spotify от имени пользователя.
• Поэтому: шифрование, Keychain, отсутствие логирования, HTTPS.

==============================================================
2. ЛИЦЕНЗИЯ SPOTIFY API — ПОЛНОЕ ОБЪЯСНЕНИЕ
==============================================================

