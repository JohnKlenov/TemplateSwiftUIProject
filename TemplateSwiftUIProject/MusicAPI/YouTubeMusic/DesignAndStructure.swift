APP CONCEPT & DATA MODEL — PLAYLISTS, TRACKS, PLAYER, FILTERS
==============================================================

1. ПЕРВЫЙ ЭТАП: ТЕСТОВЫЙ ЭКРАН ПЛЕЙЛИСТА + ПЛЕЕР
--------------------------------------------------------------

Цель:
- Создать тестовый экран со списком треков из одного плейлиста.
- Фетчить данные плейлиста из Firestore.
- Отображать список треков (кастомная ячейка, дизайн будет позже).
- При нажатии на трек — открывать полноэкранный плеер.

Технически:
- Коллекция: playlists/{playlistId}/tracks/{trackId}
- Модель трека: TrackMetadata (+ дополнительные поля в будущем).
- Экран:
  - PlaylistTracksView: список треков.
  - При тапе по ячейке → открывается PlayerView (full screen).

Плеер:
- На первом этапе:
  - Используем YouTube IFrame Player внутри WKWebView.
  - Загружаем по videoId (loadVideoById).
  - Можно начать с простого встраивания IFrame без кастомного UI.
- На следующем этапе:
  - Кастомный UI поверх IFrame:
    - Название трека
    - Артист
    - Длительность
    - Прогресс-бар
    - Кнопки Play / Pause / Next / Like
  - Управление через JS API:
    - player.playVideo()
    - player.pauseVideo()
    - player.seekTo()
    - player.getCurrentTime()
    - player.getDuration()

Важно:
- Воспроизведение через IFrame Player ≠ SafariView.
- SafariView — это просто браузер.
- IFrame Player — управляемый плеер внутри нашего UI.

План:
- Сначала: просто запустить IFrame Player с videoId.
- Потом: обернуть его кастомным UI и связать с JS API.


2. ЭКРАН PLAYLISTS — "СТЕНА" С ПЛЕЙЛИСТАМИ
--------------------------------------------------------------

Идея:
- Экран Playlists — это "стена" (как лента ВКонтакте).
- Каждый пост на стене — это:
  - Картинка (обложка плейлиста / поста).
  - Название плейлиста.
  - Описание.
  - Список треков (либо сразу, либо по кнопке "Показать").

Типы постов:
- Основная категория:
  - Плейлисты из свежих альбомов, вышедших в последние месяцы.
  - Это "основной контент" ленты.
- Дополнительные посты:
  - Подборка лучших треков артиста.
  - Лучшие треки года (по версии издания / приложения).
  - Тематические подборки (GYM, Party, Relax и т.д.).
  - Любые другие "особые" посты.

Фильтры:
- По жанру: Rap, HipHop, R&B, etc.
- По типу: GYM, Party, Relax, etc.
- По "цвету энергии":
  - Огненно-яркий — взрывные треки.
  - Розовый — треки для девушек.
  - Неоновый — с электронным бэкграундом.
  - Это может стать фирменным фильтром приложения.

Поиск:
- На экране Playlists должна быть строка поиска.
- Поиск по всей базе треков/плейлистов в Firestore.
- Важно:
  - Минимизировать количество запросов.
  - Не сжечь квоты.
  - Продумать структуру данных под эффективные запросы.


3. СТРУКТУРА ДАННЫХ В FIREBASE (ПЕРВИЧНАЯ МОДЕЛЬ)
--------------------------------------------------------------

Сейчас:
- Есть коллекция playlists:
  - playlists/{playlistId}
  - playlists/{playlistId}/tracks/{trackId}

Идея:
- Разделить логически:
  1) "Основные плейлисты" (свежие релизы).
  2) "Посты другого характера" (подборки, топы, спец-посты).

Варианты:
- Вариант A: одна коллекция playlists, но с типом:
  - playlists/{playlistId}
    - type: "release" | "curated" | "special"
    - title
    - description
    - coverImageURL
    - createdAt
    - energyColor
    - tags: [ "gym", "party", "rap" ]
- Вариант B: две коллекции:
  - playlists_release/{playlistId}
  - playlists_special/{playlistId}
- Вариант C: одна коллекция posts:
  - posts/{postId}
    - kind: "playlist" | "article" | "curated"
    - playlistId (опционально)
    - metadata...

Оптимальный старт:
- Оставить одну коллекцию playlists, но добавить поле type:
  - type = "release" — свежие альбомы.
  - type = "curated" — подборки, спец-посты.
- Это позволит:
  - Фильтровать по type.
  - Строить разные ленты (например, "только релизы" / "только подборки").


4. МОДЕЛЬ ТРЕКА — ДОПОЛНИТЕЛЬНЫЕ ПОЛЯ
--------------------------------------------------------------

Сейчас TrackMetadata:
- videoId: String
- title: String
- artist: String
- thumbnailURL: String
- durationISO8601: String

Нужно добавить:
- orderIndex: Int
  - Порядковый номер трека внутри плейлиста.
  - Для сохранения порядка, в котором админ добавил треки.
- genre: String
  - Например: "Rap", "HipHop", "R&B", "Pop", etc.
- energyColor: String
  - Например: "fire", "pink", "neon", etc.
  - Можно хранить как enum-строку.
- type: String (опционально)
  - Например: "album_track", "best_of", "year_top", etc.
- tags: [String]
  - Например: ["gym", "party", "usa", "2026"].

Пример документа трека:
  playlists/{playlistId}/tracks/{trackId}:
    videoId: "POaPNeSolhw"
    title: "OT The Real - DON'T DROWN"
    artist: "OT The Real"
    thumbnailURL: "https://..."
    durationISO8601: "PT2M57S"
    orderIndex: 1
    genre: "Rap"
    energyColor: "fire"
    type: "release"
    tags: ["gym", "usa", "2026"]


5. ХРОНОЛОГИЯ И ОТДЕЛЕНИЕ "ОСНОВНЫХ" И "ПРОЧИХ" ТРЕКОВ
--------------------------------------------------------------

Идея:
- В общей ленте треки должны отображаться в порядке создания.
- Но "основные" релизы и "прочие" подборки — логически разные сущности.

Вариант:
- В плейлисте:
  - orderIndex — порядок внутри плейлиста.
- В посте/плейлисте:
  - createdAt — дата создания плейлиста/поста.
- В отдельной коллекции (опционально):
  - globalTracksFeed/{docId}:
    - trackId
    - playlistId
    - createdAt
    - type
    - genre
    - energyColor
  - Это может быть денормализованный "фид" для быстрого построения ленты.

Но на старте:
- Можно использовать:
  - createdAt в playlists
  - orderIndex в tracks
- И строить ленту по плейлистам, а не по отдельным трекам.


6. ПОИСК И ФИЛЬТРЫ (FIRESTORE С ТОЧКИ ЗРЕНИЯ КВОТ)
--------------------------------------------------------------

Цели:
- Поиск по:
  - названию трека
  - артисту
  - названию плейлиста
  - жанру
  - energyColor
  - tags
- Фильтры:
  - по жанру
  - по типу плейлиста
  - по energyColor
  - по тегам

Рекомендации:
- Не пытаться делать "full-text search" только на Firestore.
- Для MVP:
  - Делать простые запросы по полям:
    - where("genre", "==", "Rap")
    - where("energyColor", "==", "fire")
    - where("type", "==", "release")
  - Для поиска по тексту (название/артист):
    - Либо ограниченный поиск (startsWith через префиксы).
    - Либо в будущем интеграция с Algolia / Meilisearch.

Важно:
- Структура данных должна быть:
  - Достаточно денормализованной для быстрых запросов.
  - Но не превращать всё в один огромный документ (это плохо для админки и лимитов Firestore).


7. АДМИНКА И УДОБСТВО РАБОТЫ С ДАННЫМИ
--------------------------------------------------------------

Для админа:
- Неудобно работать с одним огромным документом со всеми треками.
- Удобнее:
  - Коллекция playlists.
  - Внутри — коллекция tracks.
  - Плюс, возможно, отдельная коллекция posts для "стены".

Для клиента:
- Можно:
  - Фетчить плейлисты пачками (пагинация).
  - Для каждого плейлиста — подгружать треки по мере необходимости.
  - Кешировать данные локально.

Баланс:
- Админка → удобная структура (playlists + tracks).
- Клиент → оптимизированные запросы (фильтры, индексы, денормализация при необходимости).


8. ДАЛЬНЕЙШИЕ ШАГИ
--------------------------------------------------------------

1) Реализовать:
   - Экран плейлиста с треками (из Firestore).
   - Тап по треку → полноэкранный IFrame Player.
   - Минимальный рабочий плеер (без кастомного UI).

2) Добавить:
   - Поля genre, energyColor, type, orderIndex в TrackMetadata.
   - Поле type в playlists (release / curated).

3) Спроектировать:
   - Экран Playlists (стена).
   - Фильтры по genre / type / energyColor.
   - Строку поиска (MVP-уровень).

4) После MVP:
   - Подумать о денормализованной коллекции для глобального фида.
   - Подумать о внешнем поисковом сервисе (если нужно full-text).

