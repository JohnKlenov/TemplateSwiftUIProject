## 1. Цель проекта
Создать iOS‑приложение, которое:
- воспроизводит музыку через YouTube IFrame Player SDK,
- предоставляет кураторские подборки,
- позволяет собирать личные плейлисты,
- синхронизирует их с YouTube Music,
- использует Firebase как backend.

## 2. Основные модули

### 2.1. Onboarding
- Объяснение концепции.
- Пояснение ограничений YouTube.
- Призыв к авторизации Google.

### 2.2. Home Screen
- Лента кураторских плейлистов.
- Карточка плейлиста: обложка, описание, список videoId.

### 2.3. YouTube Player
- Использовать YouTube IFrame Player SDK.
- Плеер должен быть видимым (≥200x200 px).
- Кастомный UI поверх плеера:
  - Play/Pause, Next/Previous, Like/Add.
- Поддержка Picture-in-Picture (если возможно).

### 2.4. User Playlist (Firebase)
- Локальное хранение:
  - userId,
  - playlistId (локальный),
