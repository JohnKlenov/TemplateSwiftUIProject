API:
- playlists.insert — создание плейлиста.
- playlistItems.insert — добавление трека.
- playlistItems.delete — удаление трека.

Сценарий:
1. Пользователь нажимает «+».
2. Трек сохраняется в Firebase.
3. Если авторизован — отправляется playlistItems.insert.

### 2.7. Deep Link для фонового режима
- Кнопка «Слушать в фоне».
- Открывает плейлист в YouTube Music:
  - youtube://playlist?list=PLAYLIST_ID
  - https://music.youtube.com/playlist?list=PLAYLIST_ID
- Premium → работает фоновый режим.

### 2.8. Квоты YouTube API
- Не использовать поиск внутри приложения.
- Хранить videoId заранее.
- Ограничить количество добавлений.
- Кэшировать изменения.
- Запрашивать увеличение квот при росте.

