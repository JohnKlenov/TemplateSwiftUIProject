ОБЩАЯ ИДЕЯ
Приложение использует YouTube как источник аудио-контента.
Воспроизведение происходит через встроенный YouTube In-App Player.
Фоновое воспроизведение запрещено правилами YouTube API.
Приложение работает как "музыкальный навигатор" и "плейлист-менеджер".

==============================================================
ОСНОВНОЙ ФУНКЦИОНАЛ
1. Onboarding
   - Пользователь получает объяснение сути приложения.
   - Показано, что приложение помогает находить музыку, собирать плейлисты,
     сохранять треки и синхронизировать их с YouTube Music.

2. Home Screen
   - Лента плейлистов (подборки, категории, тематические группы).
   - Пользователь может прослушивать треки через YouTube Player.
   - В фоне воспроизведение недоступно (ограничение YouTube).

3. Корзина (User Playlist)
   - Пользователь может добавлять треки в "Корзину".
   - "Корзина" — это локальный плейлист, хранящийся в Firebase Firestore.
   - Анонимный пользователь может сохранять треки, но при первом добавлении
     система предлагает авторизоваться, чтобы не потерять данные.

4. Авторизация
   - Вкладка Profile позволяет авторизоваться в YouTube Music.
   - После авторизации треки из "Корзины" автоматически синхронизируются
     с плейлистом пользователя в YouTube Music.
   - Если у пользователя есть YouTube Premium:
       • он может слушать плейлисты в фоне через deep-link в YouTube Music;
       • внутри приложения — без рекламы, но всё равно без фонового режима.

5. Дополнительные функции
   - Отправка плейлиста на email в виде текстового списка.
   - В будущем: экспорт плейлистов в Spotify / Apple Music / Deezer.
   - Воспроизведение через deep-link в этих сервисах (фон доступен).

==============================================================
МОНЕТИЗАЦИЯ
- Подписка внутри приложения (~$1).
- Бесплатный пробный период.
- Ограничения для бесплатных пользователей:
  • лимит треков в плейлистах;
  • лимит добавлений в день;
  • лимит новых плейлистов в неделю.

==============================================================
ПРЕИМУЩЕСТВА ПОДХОДА
1. Нет затрат на лицензии музыкальных каталогов.
2. Нет квот и ограничений, как у Spotify/Apple Music.
3. YouTube — глобальный источник с огромным количеством контента.
4. Простая интеграция с Firebase.
5. Возможность работать с анонимными пользователями.
6. Быстрый запуск MVP без юридических барьеров.
7. Возможность расширения в сторону Spotify/Apple Music позже.

==============================================================
ОГРАНИЧЕНИЯ И РИСКИ
1. Нет фонового воспроизведения внутри приложения (жёсткое правило YouTube).
2. Нельзя убрать рекламу без YouTube Premium.
3. YouTube может ограничивать API при нарушении ToS.
4. Контент может быть UGC, возможны блокировки.
5. UX хуже, чем у Spotify/Apple Music SDK.
6. Приложение нельзя позиционировать как "музыкальный стриминг" —
