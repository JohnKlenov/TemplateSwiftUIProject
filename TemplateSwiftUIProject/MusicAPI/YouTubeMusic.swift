/*
==============================================================
YOUTUBE-BASED MUSIC APP — ПОЛНАЯ ВЫЖИМКА ИЗ 3 ЧАСТЕЙ ТЕКСТА
==============================================================

ОБЩАЯ КОНЦЕПЦИЯ
Приложение использует YouTube как бесплатный и легальный источник музыки.
Воспроизведение происходит через YouTube IFrame Player SDK.
Фоновый режим внутри приложения невозможен — это ограничение YouTube.
Приложение работает как:
- музыкальный навигатор,
- кураторская подборка,
- менеджер плейлистов,
- мост между пользователем и YouTube Music.

==============================================================
ПУТИ ЗАПУСКА БЕЗ ЛИЦЕНЗИЙ И ПЛАТНЫХ API
1. YouTube + In-App Player (основной путь)
- Бесплатный YouTube Data API (10 000 единиц в день).
- Воспроизведение через IFrame Player.
- Вся музыка мира доступна бесплатно.
- Минус: нельзя играть в фоне, видео должно быть видно.

2. Скрейпинг + Deep Linking
- Приложение хранит свои списки треков (Firebase).
- При нажатии на трек — открывает Spotify/Apple Music через deep link.
- Музыка играет в официальном приложении.
- Минус: нет собственного плеера.

3. Audius API
- Полностью бесплатный API.
- Можно стримить прямо в приложении.
- Минус: мало официального контента, в основном ремиксы и андеграунд.

4. SoundCloud Free Tier
- Иногда открывают регистрацию для разработчиков.
- Хорошо подходит для реп-культуры.
- Минус: нестабильная доступность API.

ИТОГ: лучший вариант для старта — YouTube.

==============================================================
ОГРАНИЧЕНИЯ YOUTUBE И РАБОТА С РЕКЛАМОЙ
1. Реклама
- Без входа в Google — пользователь видит рекламу.
- Блокировать рекламу запрещено.
- Premium-пользователи не видят рекламу после авторизации.

2. Фоновый режим
- YouTube SDK блокирует звук при сворачивании приложения.
- Даже Premium не помогает внутри стороннего приложения.
- Единственный легальный обход — Picture-in-Picture (PiP).

3. Требования к плееру
- Видео должно быть видно.
- Минимальный размер ~200x200 px.
- Нельзя делать плеер прозрачным или 1x1 px.

==============================================================
ПОЛУЛЕГАЛЬНЫЕ ПОДХОДЫ (НЕ РЕКОМЕНДУЮТСЯ)
1. Перехват аудиопотока через AVAudioSession
- Работает в фоне.
- Высокий риск бана в App Store.

2. Использование Invidious/Piped
- Дает прямые ссылки на аудио.
- Нарушает ToS YouTube.
- Невозможно пройти модерацию App Store.

3. Picture-in-Picture (легально)
- Видео остаётся видимым в маленьком окне.
- Звук продолжает играть.
- Работает в iOS/Android.
- Иногда требует Premium.

РЕКОМЕНДАЦИЯ:
Не пытаться делать фоновый режим в первой версии.
Использовать deep link → YouTube Music для фонового прослушивания.

==============================================================
РАБОТА С ПЛЕЙЛИСТАМИ YOUTUBE MUSIC
1. Авторизация (OAuth 2.0)
- Пользователь входит через Google.
- Приложение получает доступ к управлению плейлистами.
- Используется scope: youtube.force-ssl.

2. Создание личного плейлиста
- При первом добавлении трека приложение вызывает playlists.insert.
- Создаётся плейлист, например: “My Rap Curated List”.
- Приложение сохраняет playlistId.

3. Добавление треков
- Локально: трек сохраняется в Firebase.
- В YouTube Music: playlistItems.insert с playlistId + videoId.
- Трек появляется в официальном приложении YouTube Music.

4. Прослушивание в фоне
- В приложении кнопка: “Слушать в фоне (YouTube Music)”.
- Deep link открывает плейлист в YouTube Music.
- Если у пользователя Premium — работает фоновый режим.

5. Типы плейлистов
- Public — видят все.
- Private — видит только владелец.
- Unlisted — доступ по ссылке (идеальный вариант).

==============================================================
КВОТЫ YOUTUBE DATA API
Бесплатно: 10 000 единиц в день.

Стоимость операций:
- Поиск (search.list) — 100 единиц.
- Добавление трека (playlistItems.insert) — 50 единиц.
- Создание плейлиста (playlists.insert) — 50 единиц.
- Авторизация — 0.
- Воспроизведение видео — 0.

ВАЖНО:
Стриминг видео НЕ тратит квоты.

Пример расхода:
100 пользователей × 1 поиск = 10 000 единиц (лимит исчерпан).
100 пользователей × 2 добавления = 10 000 единиц.

Стратегия экономии:
- Не давать пользователю поиск.
- Хранить videoId в Firebase.
- Ограничить количество добавлений в день.
- Кэшировать изменения.
- Запрашивать увеличение квот (до 100k–1M).

==============================================================
МАСШТАБИРОВАНИЕ
10 000 единиц → 200–500 активных пользователей.
100 000 единиц → 5 000–10 000 активных пользователей.
1 000 000 единиц → 100 000+ установок.

Google легко увеличивает квоты, если приложение:
- легальное,
- имеет Privacy Policy,
- не нарушает ToS.

==============================================================
UX-РЕКОМЕНДАЦИИ
