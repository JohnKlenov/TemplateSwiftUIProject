Нужно понять:
- когда показывается видео,
- когда показывается обложка (Art Track),
- как и где можно использовать snippet.thumbnails.high.url,
- что можно и нельзя кастомизировать.

--------------------------------------------------------------
1. ГЛАВНОЕ ПРАВИЛО
--------------------------------------------------------------
Если у videoId есть видеоряд (клип, видео) — YouTube ВСЕГДА показывает видео.
Разработчик НЕ может:
- заменить видео на свою картинку,
- скрыть видеоряд и показать только thumbnail,
- подменить Art Track на свой UI,
- показывать thumbnail вместо видео, если видео существует.

=> Если есть видео — оно обязано быть видно в IFrame Player.

--------------------------------------------------------------
2. КОГДА ПОЯВЛЯЕТСЯ ART TRACK
--------------------------------------------------------------
Если у videoId нет клипа, но есть аудио-трек (официальный Art Track):
- YouTube сам показывает статичную обложку (Art Track) внутри плеера.
- Это может быть обложка альбома или простая картинка.

Разработчик НЕ вставляет обложку вручную в плеер — это делает сам YouTube.

--------------------------------------------------------------
3. РОЛЬ snippet.thumbnails.high.url
--------------------------------------------------------------
snippet.thumbnails.high.url — это thumbnail (превью-картинка) видео/трека.

ВАЖНО:
- ЭТО НЕ КАРТИНКА ДЛЯ ВСТАВКИ В ПЛЕЕР.
- ЭТО КАРТИНКА ДЛЯ ТВОЕГО UI ВНЕ ПЛЕЕРА.

ГДЕ ИСПОЛЬЗОВАТЬ thumbnail:
- в списке треков,
- в карточках подборок,
- в превью плейлистов,
- в мини-плеере,
- в заглушках/загрузочных экранах.

ГДЕ НЕЛЬЗЯ ИСПОЛЬЗОВАТЬ thumbnail:
- вместо видеоряда внутри YouTube IFrame Player,
- как подмену Art Track,
- как замену видео, если оно существует.

--------------------------------------------------------------
4. ПОВЕДЕНИЕ В ЗАВИСИМОСТИ ОТ videoId
--------------------------------------------------------------
// Сценарий A — у трека есть клип
videoId → YouTube IFrame Player показывает видео.
Разработчик не может заменить его на картинку.

// Сценарий B — у трека нет клипа, но есть Art Track
videoId → YouTube IFrame Player показывает статичную обложку (Art Track).
Разработчик не подставляет thumbnail вручную — это делает YouTube.

// Сценарий C — кастомный UI вокруг плеера
Разработчик использует thumbnail (snippet.thumbnails.high.url) только
в своём интерфейсе:
- под плеером,
- над плеером,
- в списках и карточках.

--------------------------------------------------------------
5. ОГРАНИЧЕНИЯ YOUTUBE (ToS)
--------------------------------------------------------------
НЕЛЬЗЯ:
- скрывать видеоряд,
- заменять видеоряд на свою картинку,
- делать плеер прозрачным,
- уменьшать плеер до 1x1 px,
- воспроизводить только аудио без видимого видео,
- блокировать рекламу.

МОЖНО:
- скрыть стандартные контролы (controls: 0),
- оставить только видео/Art Track,
- строить свой кастомный UI вокруг плеера,
- управлять плеером через JS API.

--------------------------------------------------------------
6. РЕКОМЕНДУЕМЫЙ UI-ПАТТЕРН
--------------------------------------------------------------
Правильная архитектура экрана:

[ YouTube IFrame Player ]
  - показывает видео или Art Track
  - контролы YouTube скрыты (controls: 0)
-----------------------------------------
[ Название трека ]        // из snippet.title
[ Артист ]                // из snippet.channelTitle или парсинг title
[ Длительность ]          // из contentDetails.duration (ISO → mm:ss)
[ Твой кастомный прогресс-бар ]
[ Твои кнопки: Play / Pause / Next / + ]

thumbnail (snippet.thumbnails.high.url) используется:
- в списках,
- в карточках,
- в мини-плеере,
но НЕ внутри самого IFrame Player.

--------------------------------------------------------------
ИТОГ:
- YouTube IFrame Player ВСЕГДА отвечает за отображение видео/Art Track.
- Разработчик НЕ может подменять видеоряд на thumbnail.
- snippet.thumbnails.high.url — это ресурс для кастомного UI, а не для плеера.
- Плеер видим, контролы можно скрыть, UI вокруг — полностью кастомный.
==============================================================
*/












/*
==============================================================
МОЖНО ЛИ СРАЗУ ПЕРЕЙТИ В МИНИ-ПЛЕЕР БЕЗ ВИДЕО?
==============================================================

НЕТ. Нельзя запускать звук YouTube, не показывая видео.
Правило YouTube: если IFrame Player воспроизводит звук — видео
должно быть видимым и не скрытым.

Мини-плеер МОЖЕТ быть без видео, но только если звук идёт из
большого плеера, который остаётся на экране и показывает видео.

=> Нельзя: нажать трек → скрыть видео → показать только мини-плеер.
=> Можно: нажать трек → открыть большой плеер → свернуть → мини-плеер,
   но большой плеер остаётся видимым.
==============================================================
*/


/*
YouTube Music может сворачивать видео в маленькое окно (~50x50),
потому что использует приватный нативный плеер Google.

Мы используем YouTube IFrame Player, и по правилам YouTube:
- видео должно оставаться видимым,
- нельзя уменьшать плеер до невидимого размера,
- нельзя скрывать видео, если звук воспроизводится.

=> Нельзя повторить поведение YouTube Music.
=> Мини-плеер может быть без видео, но большой плеер должен оставаться видимым.
*/




/*
==============================================================
ВЫЖИМКА: МИНИМАЛЬНЫЙ РАЗМЕР ВИДЕО + СПИСОК ТРЕКОВ + THUMBNAIL
==============================================================

1) МИНИМАЛЬНЫЙ РАЗМЕР ВИДЕО (IFrame Player)
--------------------------------------------------------------
YouTube не даёт точного числа, но требует, чтобы видео оставалось
"clearly visible". На практике безопасный минимум:
≈ 150–180 px по высоте.

=> 50×50 или около того — считается скрытием видео и запрещено.

2) МОЖНО ЛИ ПОКАЗЫВАТЬ ВИДЕО И СПИСОК ТРЕКОВ НА ОДНОМ ЭКРАНЕ?
--------------------------------------------------------------
ДА. Это разрешено, если:
- видео остаётся видимым,
- плеер не перекрыт,
- размер не уменьшён до “невидимого”.

=> Можно сделать экран: сверху видео (≈150–180 px), снизу — скролл списка
треков плейлиста.

3) КАКИЕ КАРТИНКИ ИСПОЛЬЗОВАТЬ В СПИСКАХ (Home, плейлисты, рекомендации)
--------------------------------------------------------------
Использовать только:
snippet.thumbnails.high.url

Это официальный thumbnail, который YouTube предоставляет для превью.

НЕЛЬЗЯ:
- показывать первый кадр видео,
- генерировать кадры из видео,
- подменять видеоряд.

=> В списках всегда используется thumbnail, а не кадр из видео.

==============================================================
ИТОГ:
- Минимальный безопасный размер видео: ~150–180 px.
- Видео + список треков на одном экране — разрешено.
- В списках треков всегда показывается thumbnail из YouTube API.
==============================================================
