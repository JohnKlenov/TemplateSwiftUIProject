==============================================================
ЮРИДИЧЕСКИЕ ТРЕБОВАНИЯ YOUTUBE
- Нельзя скачивать аудио.
- Нельзя скрывать рекламу.
- Нельзя воспроизводить в фоне.
- Нельзя предоставлять прямой доступ к аудио-потоку.
- Нужно использовать официальный YouTube Player API.

При соблюдении этих правил подход легален.

==============================================================
РЕЦЕНЗИЯ (АНАЛИЗ ЖИЗНЕСПОСОБНОСТИ)
Подход имеет право на жизнь и широко используется:
- YouTube даёт огромный каталог без лицензий.
- Нет квот, нет ограничений на масштаб.
- Можно быстро выйти на рынок и протестировать идею.
