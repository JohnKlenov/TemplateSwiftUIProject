- Синхронизация с YouTube Music после авторизации.

4. Deep Link
- Кнопка “Слушать в фоне”.
- Открывает плейлист в YouTube Music.

==============================================================
МОНЕТИЗАЦИЯ
- Подписка внутри приложения (~$1).
- Бесплатный пробный период.
- Ограничения для free-версии:
  • лимит треков в день,
